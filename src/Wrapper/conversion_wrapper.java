package Wrapper;

public class conversion_wrapper {
    public static void main(String[] args){

        int a=10;
        Integer b=a;
        System.out.println(b.intValue());
        System.out.println(b.MAX_VALUE);

        System.out.println("----------------");


        Integer c=15;
        int d=c;
        System.out.println(d);

        System.out.println("------------");

        int e=17;
        Integer f=e;
        System.out.println(f.toString());

        System.out.println("--------------");

        String y="yuva";
        System.out.println(y.valueOf(y));

    }
}
